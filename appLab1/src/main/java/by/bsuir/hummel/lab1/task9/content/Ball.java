package by.bsuir.hummel.lab1.task9.content;

import java.awt.*;

public class Ball {
	private int mass;
	private Color color;

	public Ball(int mass, Color color) {
		this.mass = mass;
		this.color = color;
	}

	public int getMass() {
		return mass;
	}

	public Color getColor() {
		return color;
	}
}
